#!/usr/bin/env python3
"""
Bulk-load a pre-embedded log dataset into Qdrant and/or Elasticsearch.

Reads a parquet file (generated by generate_dataset.py) and upserts records
into the configured backends. Reads connection info from .env.

Usage:
    python seed.py logs-1m.parquet
    python seed.py logs-1m.parquet --backend qdrant
    python seed.py logs-1m.parquet --backend elasticsearch --limit 100000
"""

import argparse
import os
import sys
import time
from pathlib import Path

import polars as pl
from tqdm import tqdm


def load_env(env_path: str = ".env") -> dict:
    env = os.environ.copy()
    path = Path(env_path)
    if path.exists():
        for line in path.read_text().splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            if line.startswith("export "):
                line = line[7:]
            key, _, value = line.partition("=")
            env[key.strip()] = value.strip()
    return env


def seed_qdrant(df: pl.DataFrame, env: dict, batch_size: int = 1000,
                index_mode: str = "keyword"):
    from qdrant_client import QdrantClient, models

    url = env.get("QDRANT_URL", "http://localhost:6333")
    # use REST port for Python client
    url = url.replace(":6334", ":6333")
    api_key = env.get("QDRANT_API_KEY")
    client = QdrantClient(url=url, api_key=api_key)
    collection = "logs"

    # build vector config based on index mode
    vectors_config = {}
    sparse_vectors_config = {}

    if index_mode in ("vector", "hybrid"):
        dim = len(df["embedding"][0])
        vectors_config["dense"] = models.VectorParams(
            size=dim, distance=models.Distance.COSINE,
        )
    if index_mode in ("keyword", "hybrid"):
        sparse_vectors_config["bm25"] = models.SparseVectorParams(
            modifier=models.Modifier.IDF,
        )

    # recreate collection
    if client.collection_exists(collection):
        client.delete_collection(collection)

    client.create_collection(
        collection_name=collection,
        vectors_config=vectors_config or None,
        sparse_vectors_config=sparse_vectors_config or None,
    )

    # create payload indexes
    client.create_payload_index(collection, "service", models.PayloadSchemaType.KEYWORD)
    client.create_payload_index(collection, "level", models.PayloadSchemaType.KEYWORD)

    # bulk upsert
    total = len(df)
    print(f"Seeding Qdrant ({url}) with {total:,} records (mode={index_mode})...")
    t0 = time.time()

    for i in tqdm(range(0, total, batch_size), desc="Qdrant"):
        batch = df.slice(i, batch_size)
        points = []
        for row in batch.iter_rows(named=True):
            vectors = {}
            if index_mode in ("vector", "hybrid"):
                vectors["dense"] = row["embedding"]
            # note: BM25 sparse vectors are built by Qdrant server-side
            # via document inference, not provided by the client

            points.append(models.PointStruct(
                id=row["id"],
                vector=vectors if vectors else {},
                payload={
                    "service": row["service"],
                    "level": row["level"],
                    "message": row["message"],
                    "timestamp": row["timestamp"].isoformat(),
                },
            ))
        client.upsert(collection_name=collection, points=points)

    elapsed = time.time() - t0
    print(f"  Qdrant: {total:,} records in {elapsed:.1f}s ({total/elapsed:.0f} records/s)")


def seed_elasticsearch(
    df: pl.DataFrame,
    env: dict,
    batch_size: int = 1000,
    index_mode: str = "keyword"
):
    from elasticsearch import Elasticsearch, helpers

    url = env.get("ELASTIC_URL", "http://localhost:9200")
    user = env.get("ELASTIC_USER", "elastic")
    password = env.get("ELASTIC_PASSWORD", "changeme")
    client = Elasticsearch(url, basic_auth=(user, password))
    index = "logs"

    # build mappings
    properties = {
        "timestamp": {"type": "date"},
        "service": {"type": "keyword"},
        "level": {"type": "keyword"},
        "message": {"type": "text"},
    }
    if index_mode in ("vector", "hybrid"):
        dim = len(df["embedding"][0])
        properties["dense"] = {
            "type": "dense_vector",
            "dims": dim,
            "index": True,
            "index_options": {"type": "hnsw"},
        }

    # recreate index
    if client.indices.exists(index=index):
        client.indices.delete(index=index)

    client.indices.create(index=index, mappings={"properties": properties})

    # bulk index
    total = len(df)
    print(f"Seeding Elasticsearch ({url}) with {total:,} records (mode={index_mode})...")
    t0 = time.time()

    def gen_actions():
        for row in df.iter_rows(named=True):
            doc = {
                "_index": index,
                "_id": row["id"],
                "_routing": row["id"],
                "timestamp": row["timestamp"].isoformat(),
                "service": row["service"],
                "level": row["level"],
                "message": row["message"],
            }
            if index_mode in ("vector", "hybrid"):
                doc["dense"] = row["embedding"]
            yield doc

    successes, errors = 0, []
    for ok, item in tqdm(
        helpers.streaming_bulk(client, gen_actions(), chunk_size=batch_size, raise_on_error=False),
        total=total, desc="Elasticsearch",
    ):
        if ok:
            successes += 1
        else:
            errors.append(item)

    elapsed = time.time() - t0
    print(f"  Elasticsearch: {successes:,} records in {elapsed:.1f}s ({successes/elapsed:.0f} records/s)")
    if errors:
        print(f"  {len(errors)} errors (first: {errors[0]})", file=sys.stderr)


def seed_pgvector(df: pl.DataFrame, env: dict, batch_size: int = 1000):
    import psycopg2
    from pgvector.psycopg2 import register_vector

    host = env.get("PGVECTOR_HOST", "localhost")
    user = env.get("PGVECTOR_USER", "postgres")
    password = env.get("PGVECTOR_PASSWORD", "changeme")
    conn = psycopg2.connect(host=host, user=user, password=password, dbname="logs", port=5432)
    register_vector(conn)
    cur = conn.cursor()

    # ensure table exists
    dim = len(df["embedding"][0])
    cur.execute(f"""
        CREATE TABLE IF NOT EXISTS logs (
            id TEXT PRIMARY KEY,
            timestamp TIMESTAMPTZ,
            service TEXT,
            level TEXT,
            message TEXT,
            embedding vector({dim})
        )
    """)
    cur.execute("TRUNCATE logs")
    conn.commit()

    total = len(df)
    print(f"Seeding pgvector ({host}) with {total:,} records...")
    t0 = time.time()

    import numpy as np
    for i in tqdm(range(0, total, batch_size), desc="pgvector"):
        batch = df.slice(i, batch_size)
        values = []
        for row in batch.iter_rows(named=True):
            values.append((
                row["id"],
                row["timestamp"].isoformat(),
                row["service"],
                row["level"],
                row["message"],
                np.array(row["embedding"], dtype=np.float32),
            ))
        args_str = ",".join(
            cur.mogrify("(%s, %s, %s, %s, %s, %s)", v).decode() for v in values
        )
        cur.execute(f"INSERT INTO logs (id, timestamp, service, level, message, embedding) VALUES {args_str}")
        conn.commit()

    elapsed = time.time() - t0
    print(f"  pgvector: {total:,} records in {elapsed:.1f}s ({total/elapsed:.0f} records/s)")
    cur.close()
    conn.close()


def main():
    parser = argparse.ArgumentParser(description="Seed backends from pre-embedded parquet dataset")
    parser.add_argument(
        "parquet",
        help="Path to parquet file")
    parser.add_argument(
        "--backend",
        choices=["qdrant", "elasticsearch", "pgvector"],
        action="append", help="Backends to seed (default: all configured in .env)"
    )
    parser.add_argument(
        "--limit",
        type=int,
        help="Max records to load"
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=1000,
        help="Batch size for bulk ops"
    )
    parser.add_argument(
        "--index-mode",
        default="keyword",
        choices=["vector", "keyword", "hybrid"],
        help="Index mode (default: keyword)"
    )
    parser.add_argument(
        "--env",
        default=".env",
        help="Path to .env file"
    )
    args = parser.parse_args()

    env = load_env(args.env)

    print(f"Loading {args.parquet}...")
    df = pl.read_parquet(args.parquet)
    if args.limit:
        df = df.head(args.limit)
    print(f"  {len(df):,} records, {len(df['embedding'][0])}-dim embeddings")

    # auto-detect backends from .env if not specified
    backends = args.backend
    if not backends:
        backends = []
        if env.get("QDRANT_URL"):
            backends.append("qdrant")
        if env.get("ELASTIC_URL"):
            backends.append("elasticsearch")
        if env.get("PGVECTOR_HOST"):
            backends.append("pgvector")
        if not backends:
            # fallback to local defaults
            backends = ["qdrant", "elasticsearch"]

    for backend in backends:
        if backend == "qdrant":
            seed_qdrant(df, env, args.batch_size, args.index_mode)
        elif backend == "elasticsearch":
            seed_elasticsearch(df, env, args.batch_size, args.index_mode)
        elif backend == "pgvector":
            seed_pgvector(df, env, args.batch_size)

    print("\nDone. Let databases settle for a few seconds before running bench.py --skip-load")


if __name__ == "__main__":
    main()
