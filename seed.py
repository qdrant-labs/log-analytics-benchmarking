#!/usr/bin/env python3
"""
Bulk-load a pre-embedded log dataset into Qdrant and/or Elasticsearch.

Reads a parquet file (generated by generate_dataset.py) and upserts records
into the configured backends. Reads connection info from .env.

Usage:
    python seed.py logs-1m.parquet
    python seed.py logs-1m.parquet --backend qdrant
    python seed.py logs-1m.parquet --backend elasticsearch --limit 100000
"""

import argparse
import asyncio
import os
import sys
import time
from pathlib import Path

import polars as pl
from tqdm import tqdm


def load_env(env_path: str = ".env") -> dict:
    env = os.environ.copy()
    path = Path(env_path)
    if path.exists():
        for line in path.read_text().splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            if line.startswith("export "):
                line = line[7:]
            key, _, value = line.partition("=")
            env[key.strip()] = value.strip()
    return env


async def seed_qdrant(df: pl.DataFrame, env: dict, batch_size: int = 1000,
                      index_mode: str = "keyword"):
    from qdrant_client import AsyncQdrantClient, models

    url = env.get("QDRANT_URL", "http://localhost:6333")
    # use REST port for Python client
    url = url.replace(":6334", ":6333")
    api_key = env.get("QDRANT_API_KEY")
    client = AsyncQdrantClient(url=url, api_key=api_key)
    collection = "logs"

    # build vector config based on index mode
    vectors_config = {}
    sparse_vectors_config = {}

    if index_mode in ("vector", "hybrid"):
        dim = len(df["embedding"][0])
        vectors_config["dense"] = models.VectorParams(
            size=dim, distance=models.Distance.COSINE,
        )
    if index_mode in ("keyword", "hybrid"):
        sparse_vectors_config["bm25"] = models.SparseVectorParams(
            modifier=models.Modifier.IDF,
        )

    # recreate collection
    if await client.collection_exists(collection):
        await client.delete_collection(collection)

    await client.create_collection(
        collection_name=collection,
        vectors_config=vectors_config or None,
        sparse_vectors_config=sparse_vectors_config or None,
    )

    # create payload indexes
    await client.create_payload_index(collection, "service", models.PayloadSchemaType.KEYWORD)
    await client.create_payload_index(collection, "level", models.PayloadSchemaType.KEYWORD)

    # bulk upsert
    total = len(df)
    print(f"Seeding Qdrant ({url}) with {total:,} records (mode={index_mode})...")
    t0 = time.time()

    for i in tqdm(range(0, total, batch_size), desc="Qdrant"):
        batch = df.slice(i, batch_size)
        points = []
        for row in batch.iter_rows(named=True):
            vectors = {}
            if index_mode in ("vector", "hybrid"):
                vectors["dense"] = row["embedding"]
            # note: BM25 sparse vectors are built by Qdrant server-side
            # via document inference, not provided by the client

            points.append(models.PointStruct(
                id=row["id"],
                vector=vectors if vectors else {},
                payload={
                    "service": row["service"],
                    "level": row["level"],
                    "message": row["message"],
                    "timestamp": row["timestamp"].isoformat(),
                },
            ))
        await client.upsert(collection_name=collection, points=points)

    elapsed = time.time() - t0
    print(f"  Qdrant: {total:,} records in {elapsed:.1f}s ({total/elapsed:.0f} records/s)")
    await client.close()


async def seed_elasticsearch(
    df: pl.DataFrame,
    env: dict,
    batch_size: int = 1000,
    index_mode: str = "keyword"
):
    from elasticsearch import AsyncElasticsearch, helpers

    url = env.get("ELASTIC_URL", "http://localhost:9200")
    user = env.get("ELASTIC_USER", "elastic")
    password = env.get("ELASTIC_PASSWORD", "changeme")
    client = AsyncElasticsearch(url, basic_auth=(user, password))
    index = "logs"

    # build mappings
    properties = {
        "timestamp": {"type": "date"},
        "service": {"type": "keyword"},
        "level": {"type": "keyword"},
        "message": {"type": "text"},
    }
    if index_mode in ("vector", "hybrid"):
        dim = len(df["embedding"][0])
        properties["dense"] = {
            "type": "dense_vector",
            "dims": dim,
            "index": True,
            "index_options": {"type": "hnsw"},
        }

    # recreate index
    if await client.indices.exists(index=index):
        await client.indices.delete(index=index)

    await client.indices.create(index=index, mappings={"properties": properties})

    # bulk index
    total = len(df)
    print(f"Seeding Elasticsearch ({url}) with {total:,} records (mode={index_mode})...")
    t0 = time.time()

    def gen_actions():
        for row in df.iter_rows(named=True):
            doc = {
                "_index": index,
                "_id": row["id"],
                "_routing": row["id"],
                "timestamp": row["timestamp"].isoformat(),
                "service": row["service"],
                "level": row["level"],
                "message": row["message"],
            }
            if index_mode in ("vector", "hybrid"):
                doc["dense"] = row["embedding"]
            yield doc

    successes, errors = 0, []
    async for ok, item in helpers.async_streaming_bulk(
        client, gen_actions(), chunk_size=batch_size, raise_on_error=False
    ):
        if ok:
            successes += 1
        else:
            errors.append(item)

    elapsed = time.time() - t0
    print(f"  Elasticsearch: {successes:,} records in {elapsed:.1f}s ({successes/elapsed:.0f} records/s)")
    if errors:
        print(f"  {len(errors)} errors (first: {errors[0]})", file=sys.stderr)
    await client.close()


async def seed_opensearch(df: pl.DataFrame, env: dict, batch_size: int = 1000,
                          index_mode: str = "keyword"):
    from opensearchpy import AsyncOpenSearch, helpers

    url = env.get("OPENSEARCH_URL", "http://localhost:9200")
    user = env.get("OPENSEARCH_USER", "admin")
    password = env.get("OPENSEARCH_PASSWORD", "Changeme1!")
    # parse host/port from URL
    from urllib.parse import urlparse
    parsed = urlparse(url)
    client = AsyncOpenSearch(
        hosts=[{"host": parsed.hostname, "port": parsed.port or 9200}],
        http_auth=(user, password),
        use_ssl=parsed.scheme == "https",
        verify_certs=False,
    )
    index = "logs"

    # build mappings
    properties = {
        "timestamp": {"type": "date"},
        "service": {"type": "keyword"},
        "level": {"type": "keyword"},
        "message": {"type": "text"},
    }
    settings = {}
    if index_mode in ("vector", "hybrid"):
        dim = len(df["embedding"][0])
        properties["dense"] = {
            "type": "knn_vector",
            "dimension": dim,
            "method": {
                "name": "hnsw",
                "space_type": "cosinesimil",
                "engine": "nmslib",
            },
        }
        settings["index"] = {"knn": True}

    # recreate index
    if await client.indices.exists(index=index):
        await client.indices.delete(index=index)

    body = {"mappings": {"properties": properties}}
    if settings:
        body["settings"] = settings
    await client.indices.create(index=index, body=body)

    # bulk index
    total = len(df)
    print(f"Seeding OpenSearch ({url}) with {total:,} records (mode={index_mode})...")
    t0 = time.time()

    def gen_actions():
        for row in df.iter_rows(named=True):
            doc = {
                "_index": index,
                "_id": row["id"],
                "timestamp": row["timestamp"].isoformat(),
                "service": row["service"],
                "level": row["level"],
                "message": row["message"],
            }
            if index_mode in ("vector", "hybrid"):
                doc["dense"] = row["embedding"]
            yield doc

    successes, errors = 0, []
    async for ok, item in helpers.async_streaming_bulk(
        client, gen_actions(), chunk_size=batch_size, raise_on_error=False
    ):
        if ok:
            successes += 1
        else:
            errors.append(item)

    elapsed = time.time() - t0
    print(f"  OpenSearch: {successes:,} records in {elapsed:.1f}s ({successes/elapsed:.0f} records/s)")
    if errors:
        print(f"  {len(errors)} errors (first: {errors[0]})", file=sys.stderr)
    await client.close()


async def seed_pgvector(df: pl.DataFrame, env: dict, batch_size: int = 1000):
    from psycopg import AsyncConnection
    from pgvector.psycopg import register_vector_async

    host = env.get("PGVECTOR_HOST", "localhost")
    user = env.get("PGVECTOR_USER", "postgres")
    password = env.get("PGVECTOR_PASSWORD", "changeme")
    conn = await AsyncConnection.connect(
        host=host, user=user, password=password, dbname="logs", port=5432,
        autocommit=False,
    )
    await register_vector_async(conn)
    cur = conn.cursor()

    # ensure table exists
    dim = len(df["embedding"][0])
    await cur.execute(f"""
        CREATE TABLE IF NOT EXISTS logs (
            id TEXT PRIMARY KEY,
            timestamp TIMESTAMPTZ,
            service TEXT,
            level TEXT,
            message TEXT,
            embedding vector({dim})
        )
    """)
    await cur.execute("TRUNCATE logs")
    await conn.commit()

    total = len(df)
    print(f"Seeding pgvector ({host}) with {total:,} records...")
    t0 = time.time()

    import numpy as np
    for i in tqdm(range(0, total, batch_size), desc="pgvector"):
        batch = df.slice(i, batch_size)
        async with cur.copy("COPY logs (id, timestamp, service, level, message, embedding) FROM STDIN") as copy:
            for row in batch.iter_rows(named=True):
                emb = np.array(row["embedding"], dtype=np.float32)
                await copy.write_row((
                    row["id"],
                    row["timestamp"].isoformat(),
                    row["service"],
                    row["level"],
                    row["message"],
                    emb,
                ))
        await conn.commit()

    elapsed = time.time() - t0
    print(f"  pgvector: {total:,} records in {elapsed:.1f}s ({total/elapsed:.0f} records/s)")
    await cur.close()
    await conn.close()


async def async_main(args):
    env = load_env(args.env)

    parquet_path = Path(args.parquet)
    if parquet_path.is_dir():
        print(f"Loading chunks from {parquet_path}/...")
        df = pl.read_parquet(parquet_path / "*.parquet")
    else:
        print(f"Loading {parquet_path}...")
        df = pl.read_parquet(parquet_path)
    if args.limit:
        df = df.head(args.limit)
    print(f"  {len(df):,} records, {len(df['embedding'][0])}-dim embeddings")

    # auto-detect backends from .env if not specified
    backends = args.backend
    if not backends:
        backends = []
        if env.get("QDRANT_URL"):
            backends.append("qdrant")
        if env.get("ELASTIC_URL"):
            backends.append("elasticsearch")
        if env.get("OPENSEARCH_URL"):
            backends.append("opensearch")
        if env.get("PGVECTOR_HOST"):
            backends.append("pgvector")
        if not backends:
            # fallback to local defaults
            backends = ["qdrant", "elasticsearch"]

    seed_fns = {
        "qdrant": lambda: seed_qdrant(df, env, args.batch_size, args.index_mode),
        "elasticsearch": lambda: seed_elasticsearch(df, env, args.batch_size, args.index_mode),
        "opensearch": lambda: seed_opensearch(df, env, args.batch_size, args.index_mode),
        "pgvector": lambda: seed_pgvector(df, env, args.batch_size),
    }

    # run all backends concurrently
    coros = [_timed(backend, seed_fns[backend]()) for backend in backends]

    timings = {}
    results = await asyncio.gather(*coros, return_exceptions=True)

    for name, result in zip(backends, results):
        if isinstance(result, Exception):
            print(f"  {name}: FAILED ({result})", file=sys.stderr)
            timings[name] = 0
        else:
            timings[name] = result

    print("\n--- Seeding summary ---")
    for backend in backends:
        elapsed = timings.get(backend, 0)
        print(f"  {backend}: {elapsed:.1f}s")
    print(f"  total wall-clock: {sum(timings.values()):.1f}s")
    print("\nDone. Let databases settle for a few seconds before running bench.py")


async def _timed(name: str, coro) -> float:
    t0 = time.time()
    await coro
    return time.time() - t0


def main():
    parser = argparse.ArgumentParser(description="Seed backends from pre-embedded parquet dataset")
    parser.add_argument(
        "parquet",
        help="Path to parquet file or directory of parquet chunks")
    parser.add_argument(
        "--backend",
        choices=["qdrant", "elasticsearch", "opensearch", "pgvector"],
        action="append", help="Backends to seed (default: all configured in .env)"
    )
    parser.add_argument(
        "--limit",
        type=int,
        help="Max records to load"
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=1000,
        help="Batch size for bulk ops"
    )
    parser.add_argument(
        "--index-mode",
        default="hybrid",
        choices=["vector", "keyword", "hybrid"],
        help="Index mode (default: keyword)"
    )
    parser.add_argument(
        "--env",
        default=".env",
        help="Path to .env file"
    )
    args = parser.parse_args()

    asyncio.run(async_main(args))


if __name__ == "__main__":
    main()