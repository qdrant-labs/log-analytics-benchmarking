#!/usr/bin/env python3
"""
Bulk-load a pre-embedded log dataset into one or more backends.

Reads a parquet file (generated by generate_dataset.py) and upserts records
into the configured backends. Reads connection info from .env.

Usage:
    python seed.py logs-1m.parquet
    python seed.py logs-1m.parquet --backend qdrant
    python seed.py logs-1m.parquet --backend elasticsearch --limit 100000
"""

import argparse
import asyncio
import sys
import time
from pathlib import Path

import polars as pl

from logbench import BenchConfig, get_backends, load_env
from logbench.backends import REGISTRY


async def _timed(name: str, coro) -> float:
    t0 = time.time()
    await coro
    return time.time() - t0


async def async_main(args):
    env = load_env(args.env)

    # determine index mode: CLI flag > bench config > default
    if args.config:
        config = BenchConfig.from_yaml(args.config)
        index_mode = args.index_mode or config.index_mode
    else:
        index_mode = args.index_mode or "hybrid"

    # load dataset
    parquet_path = Path(args.parquet)
    if parquet_path.is_dir():
        print(f"Loading chunks from {parquet_path}/...")
        df = pl.read_parquet(parquet_path / "*.parquet")
    else:
        print(f"Loading {parquet_path}...")
        df = pl.read_parquet(parquet_path)
    if args.limit:
        df = df.head(args.limit)
    print(f"  {len(df):,} records, {len(df['embedding'][0])}-dim embeddings")

    # determine backends: CLI flag > bench config > auto-detect from env
    backend_names = args.backend
    if not backend_names:
        if args.config:
            backend_names = config.backends
        else:
            backend_names = [
                name for name, cls in REGISTRY.items()
                if cls.from_env(env).is_configured()
            ]
        if not backend_names:
            backend_names = ["qdrant", "elasticsearch"]

    backends = get_backends(backend_names, env)

    # seed all backends concurrently
    coros = [
        _timed(b.name, b.seed(df, index_mode, args.batch_size))
        for b in backends
    ]
    results = await asyncio.gather(*coros, return_exceptions=True)

    timings = {}
    for backend, result in zip(backends, results):
        if isinstance(result, Exception):
            print(f"  {backend.name}: FAILED ({result})", file=sys.stderr)
            timings[backend.name] = 0
        else:
            timings[backend.name] = result

    print("\n--- Seeding summary ---")
    for name, elapsed in timings.items():
        print(f"  {name}: {elapsed:.1f}s")
    print(f"  total wall-clock: {sum(timings.values()):.1f}s")
    print("\nDone. Let databases settle for a few seconds before running bench.py")


def main():
    parser = argparse.ArgumentParser(description="Seed backends from pre-embedded parquet dataset")
    parser.add_argument(
        "parquet",
        help="Path to parquet file or directory of parquet chunks")
    parser.add_argument(
        "--backend",
        choices=list(REGISTRY.keys()),
        action="append", help="Backends to seed (default: all configured in .env)"
    )
    parser.add_argument(
        "--limit",
        type=int,
        help="Max records to load"
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=1000,
        help="Batch size for bulk ops"
    )
    parser.add_argument(
        "--index-mode",
        choices=["vector", "keyword", "hybrid"],
        help="Index mode (default: from bench config or hybrid)"
    )
    parser.add_argument(
        "--config",
        help="Path to bench_config.yaml (reads index_mode and backends from it)"
    )
    parser.add_argument(
        "--env",
        default=".env",
        help="Path to .env file"
    )
    args = parser.parse_args()

    asyncio.run(async_main(args))


if __name__ == "__main__":
    main()
